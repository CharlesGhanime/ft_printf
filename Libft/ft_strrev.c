#include "libft.h"

char	*ft_strrev(char *str)
{
	size_t	i;
	size_t	j;
	int		c;

	i = 0;
	j = 0;
	while (str[j] != '\0')
		j++;
	j--;
	while (i < j)
	{
		c = str[i];
		str[i] = str[j];
		str[j] = c;
		i++;
		j--;
	}
	return (str);
}
